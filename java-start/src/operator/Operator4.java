package operator;

public class Operator4 {

    public static void main(String[] args) {

        int sum3 = 2*2+3*3;
        int sum4 = (2*2)+(3*3);
        //두경우가 같지만 ()를 쓸경우 명확하게 볼수있기때문에
        //()를 쓰는게 좋음
        System.out.println("sum3="+sum3);
        System.out.println("sum4="+sum4);
    }
}
